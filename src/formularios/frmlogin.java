    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String email=txtemail.getText();
        String contrasena=txtcontrasena.getText();
        Conexion co= new Conexion();
        ArrayList<LoginGetSet> re= co.login(email, contrasena);
        
        String datos[]= new String [8];
        for(LoginGetSet log:re){
            datos[0]=log.getId();
            datos[1]=log.getTipo_documento();
            datos[2]=log.getNombre();
            datos[3]=log.getApellido();
            datos[4]=log.getFecha_nacimiento();
            datos[5]=log.getTelefono();
            datos[6]=log.getEmail();
            datos[7]=log.getContrasena();
           
        }
        
        if (re.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Acceso denegado");
        } else {
            Inicio ini= new Inicio();
            this.dispose();
            ini.setVisible(true);
            JOptionPane.showMessageDialog(null, "Login exitoso");
                    
        }
      
    }//GEN-LAST:event_jButton1ActionPerformed

